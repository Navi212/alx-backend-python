#!/usr/bin/env python3
""" The `1-concat` module supplies a function concat"""


def concat(str1: str, str2: str) -> str:
    """
    concat: Function that returns a concatenated string
            `str1` and `str2`
    Args:
    str1: str
    str2: str

    Return:
    concatenated string: str
    """
    return (str1 + str2)
